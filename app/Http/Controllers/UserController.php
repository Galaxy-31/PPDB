    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $request->validate([
            'name' => 'required',
            'email' => 'required',
            'user' => 'required',
            'password' => 'required',
            'role' => 'required',
        ]);

        $data = User::create([
            'name' => $request->name,
            'email' => $request->email,
            'user' => $request->user,
            'password' => Hash::make($request->password),
        ]);

        if ($request->role == 'admin') {
            $data->attachRole('admin');
        }else {
            $data->attachRole('petugas');
        }

        if($data) {
            toast('User Berhasil Di Buat!','success');
        }else {
            toast('User Gagal Di Buat!','error');
        }

        return redirect()->route('users.index');
    }

